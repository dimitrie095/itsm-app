const { PrismaClient } = require('@prisma/client');
const fs = require('fs');
const path = require('path');

async function main() {
  console.log('Applying PostgreSQL migration...');
  
  const prisma = new PrismaClient();
  
  try {
    // Read the migration SQL file
    const migrationPath = path.join(__dirname, '../prisma/migrations/20250320120000_sqlite_to_postgresql_migration/migration.sql');
    const sql = fs.readFileSync(migrationPath, 'utf8');
    
    // Split SQL statements by semicolon (simple approach)
    const statements = sql.split(';').filter(stmt => stmt.trim().length > 0);
    
    for (const statement of statements) {
      console.log(`Executing: ${statement.substring(0, 100)}...`);
      await prisma.$executeRawUnsafe(statement);
    }
    
    console.log('Migration applied successfully.');
    
    // Insert a record into _prisma_migrations table to mark this migration as applied
    const migrationName = '20250320120000_sqlite_to_postgresql_migration';
    const checksum = ''; // would be generated by Prisma
    const finishedAt = new Date();
    const startedAt = new Date();
    
    await prisma.$executeRawUnsafe(`
      INSERT INTO "_prisma_migrations" ("id", "checksum", "finished_at", "migration_name", "logs", "rolled_back_at", "started_at", "applied_steps_count")
      VALUES ($1, $2, $3, $4, $5, $6, $7, $8)
    `, migrationName, checksum, finishedAt, migrationName, null, null, startedAt, 1);
    
    console.log('Migration recorded in _prisma_migrations table.');
  } catch (error) {
    console.error('Error applying migration:', error);
    process.exit(1);
  } finally {
    await prisma.$disconnect();
  }
}

if (require.main === module) {
  main();
}

module.exports = { main };